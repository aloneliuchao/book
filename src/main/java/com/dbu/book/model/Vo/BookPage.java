package com.dbu.book.model.Vo;

public class BookPage {
    private int  pcount;
    private int  psize;
    private long  pindex;

    public int getPcount() {
        return pcount;
    }

    public void setPcount(int pcount) {
        this.pcount = pcount;
    }

    public int getPsize() {
        return psize;
    }

    public void setPsize(int psize) {
        this.psize = psize;
    }

    public long getPindex() {
        return pindex;
    }

    public void setPindex(long pindex) {
        this.pindex = pindex;
    }
}
